//Creating a static method to calc avg and qty of ratings for the
//tourID 4 which the current review was created
reviewSchema.statics.calcAverageRatings = async function (tourId) {
  // console.log(tourId);

  //Calling the static method on the model
  //Construction of aggregation pipeline
  const stats = await this.aggregate([
    {
      //selecting the Reviews that match the current tourId
      $match: { tour: tourId },
    },
    {
      //calculating statistics for all of the review
      $group: {
        _id: '$tour',
        nRating: { $sum: 1 },
        avgRating: { $avg: '$rating' },
      },
    },
  ]);
  // console.log(stats);

  // saving the statistics to the current tour
  if (stats.length > 0) {
    await Tour.findByIdAndUpdate(tourId, {
      ratingQuantity: stats[0].nRating,
      ratingAverage: stats[0].avgRating,
    });
  } else {
    await Tour.findByIdAndUpdate(tourId, {
      ratingQuantity: 0,
      ratingAverage: 4.5,
    });
  }
};

// calling calcAverageRatings after a new review has been created
reviewSchema.post('save', function () {
  //this points to current review
  this.constructor.calcAverageRatings(this.tour);
});

